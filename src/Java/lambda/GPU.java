package Java.lambda;

public class GPU {

    private String name;
    private int price;
    private int b;
    private int c;
    private int d;
    private int e;

    
    public GPU(String name, int price, int b, int c, int d, int e) {
        this.name = name;
        this.price = price;
        this.b = b;
        this.c = c;
        this.d = d;
        this.e = e;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getPrice() {
        return price;
    }
    public void setPrice(int price) {
        this.price = price;
    }
    public int getB() {
        return b;
    }
    public void setB(int b) {
        this.b = b;
    }
    public int getC() {
        return c;
    }
    public void setC(int c) {
        this.c = c;
    }
    public int getD() {
        return d;
    }
    public void setD(int d) {
        this.d = d;
    }
    public int getE() {
        return e;
    }
    public void setE(int e) {
        this.e = e;
    }

    
    
}
